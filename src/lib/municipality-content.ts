/**
 * 自治体のリッチコンテンツを検索する
 */
export function getMunicipalityContent(prefecture: string, city: string): MunicipalityContent | null {
  return CONTENT_DATA.find(
    (c) => c.prefecture === prefecture && c.city === city
  ) || null;
}

/**
 * リッチコンテンツが存在する自治体の一覧を取得
 */
export function getMunicipalitiesWithContent(): { prefecture: string; city: string }[] {
  return CONTENT_DATA.map((c) => ({ prefecture: c.prefecture, city: c.city }));
}
