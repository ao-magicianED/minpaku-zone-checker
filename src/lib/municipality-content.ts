/** 関連リンク */
export interface RelatedLink {
  /** リンクラベル */
  label: string;
  /** URL */
  url: string;
  /** リンクの説明（任意） */
  description?: string;
}

/** コンテンツのセクション */
export interface ContentSection {
  /** セクションタイトル */
  title: string;
  /** セクションアイコン（絵文字） */
  icon: string;
  /** セクション本文（HTML使用不可、改行は \n で表現） */
  body: string;
}

/** 自治体の詳細コンテンツ */
export interface MunicipalityContent {
  /** 都道府県 */
  prefecture: string;
  /** 市区町村 */
  city: string;
  /** ページ冒頭のリード文（2〜3文） */
  leadText: string;
  /** 規制の詳細解説セクション */
  regulationDetail: string;
  /** 区域制限の詳細解説 */
  areaRestrictionDetail: string;
  /** この自治体で民泊をするメリット・魅力 */
  attractionPoints: string;
  /** 民泊運営上の注意点・リスク */
  operationCautions: string;
  /** エリア別ガイド（エリアごとの特徴） */
  areaGuide: ContentSection[];
  /** 公式・関連リンク集 */
  relatedLinks: RelatedLink[];
  /** 追加のFAQ（ページのテンプレートFAQに追加して表示） */
  additionalFaq: { question: string; answer: string }[];
}

